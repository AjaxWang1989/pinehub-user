                    {name: '半熟芝士', key: 'cake6'},
                ]
            }
        },
        methods: {
            tabSelect(tab) {
                this.statusType = tab.key;
                this.scrollTo = tab.key
            },
            openCate () {
                this.showAllCates = true;
                this.$emit('forbidScroll', true)
            },
            closeCate () {
                this.showAllCates = false;
                this.$emit('forbidScroll', false)
            }
        }
	}
</script>

